  <h1>Pravno obvestilo spletnega mesta potep.eu</h1>
  <p>
    Vse pravice so pridržane. Obvestilo se nanaša na začetno stran in vse
    podstrani domene potep.eu. Informacije in gradiva na elektronskem naslovu
    potep.eu so zaščitene s pravicami intelektualne lastnine Mira Sodja (v
    nadaljevanju avtor).
  </p>
  <h1>Omejitev odgovornosti</h1>
  <p>
    Vse informacije in gradiva na potep.eu so informativne narave. Avtor se bo
    trudil, da bo domena potep.eu in programi na njej delovali nemoteno, vendar
    ne prevzema nikakršnih odgovornosti in garancij za nemoteno uporabo domene
    in programov na njej. Vsi uporabniki uporabljajo domeno potep.eu in programe
    na njej na lastno odgovornost. Niti avtor niti katerakoli druga fizična ali
    pravna oseba, ki je sodelovala ali sodeluje pri ustvarjanju, nastanku ali
    izdelavi potep.eu, ne odgovarja za nobeno škodo, ki bi lahko nastala zaradi
    dostopa, uporabe ali nezmožnosti uporabe informacij in gradiv na potep.eu.
    Ta omejitev odgovornosti velja tudi za kakršnekoli napake ali
    pomanjkljivosti v njihovi vsebini in za škodo ki bi utegnila nastati zaradi
    uporabe ali nezmožnosti uporabe domene ali programov. Avtor lahko te strani
    spremeni kadarkoli in brez predhodnega obvestila in ne prevzema nikakršne
    odgovornosti za posledice teh sprememb.
  </p>
  <h1>Varovanje osebnih podatkov in zasebnost spletnega mesta potep.eu</h1>
  <p>
    Brez privolitve uporabnika ali naročnika avtor ne zbira in ne obdeluje na
    tej spletni strani nobenih osebnih podatkov. Med obiskom anonimne osebe z
    anonimnega računalnika na tej spletni strani, se shrani podatek v brskalniku
    obiskovalca, da je bil na spletni strani. Vsi ostali podatki, se shranjujejo
    v brskalniku računalnika v bazi IndexedDB. Ti podatki se lahko prenesejo na
    strežnik z namenom priprave dokumentov (PDF) obiskovalcu, vendar se na
    strežniku ne shranjujejo na kakršen koli medij.
  </p>
